package Calc;


public class API {


    public float addition(float x, float y) {

        return x + y;
    }

    public float subtraction(float x, float y) {

        return x - y;
    }

    public float multiplication(float x, float y) {

        return x * y;
    }

    public float division (float x, float y) {

        return x / y;
    }


}